package com.example.demo.repository;

import com.example.demo.entity.Booking;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import java.util.List;
import java.util.Optional;

@Repository
public interface BookingRepository extends JpaRepository<Booking, Long> {
    Optional<Booking> findByReferenceNo(String referenceNo);
    List<Booking> findByUserIdOrderByCreatedAtDesc(Long userId);
    List<Booking> findByCustomerEmailOrderByCreatedAtDesc(String email);
    List<Booking> findByCustomerPhoneOrderByCreatedAtDesc(String phone);
    List<Booking> findByStatusOrderByCreatedAtDesc(Booking.BookingStatus status);
}
